        IF :NEW.IRP_ID IS NULL THEN
            :NEW.IRP_ID := 'HANA_IRP_' || TO_CHAR(SEQ_HANABANK_IRP_ACCOUNT_ID.NEXTVAL, 'FM000000000');
        END IF;
    END IF;
    IF UPDATING THEN
        :NEW.UPDATED_AT := SYSTIMESTAMP;
    END IF;
END;
/

